function bit6(i) {
    const bit5 = (i >> 5) & 1;
    const bit7 = (i >> 7) & 1;
    const bit6 = (bit5 | bit7) ^ 1;

    return (i & 0xBF) | (bit6 << 6);
}

function makeImage(image) {
    const TOP_MARGIN = 10;
    const BOTTOM_MARGIN = 6; // Six less because the glyphs are black for bottom 4 pixels.
    const SIDE_MARGIN = 10;
    const CH_WIDTH = image.width;
    const CH_HEIGHT = 12;
    const glyphs = image.glyphs.slice(image.first*CH_HEIGHT, (image.first + image.count)*CH_HEIGHT);
    const GLYPH_COUNT = glyphs.length / CH_HEIGHT;
    const COLS = 32;
    const ROWS = Math.ceil(GLYPH_COUNT / COLS);
    const WIDTH = CH_WIDTH * COLS + SIDE_MARGIN*2;
    const HEIGHT = CH_HEIGHT * ROWS * 2 + TOP_MARGIN + BOTTOM_MARGIN;

    const dst = new PNG({
        width: WIDTH,
        height: HEIGHT,
    });

    // Fill background with black (mostly for the margins).
    for (let i = 0; i < WIDTH*HEIGHT; i++) {
        const offset = i*4;

        dst.data[offset + 0] = 0;
        dst.data[offset + 1] = 0;
        dst.data[offset + 2] = 0;
        dst.data[offset + 3] = 0xFF;
    }

    for (let i = 0; i < GLYPH_COUNT; i++) {
        const cx = i % COLS * CH_WIDTH + SIDE_MARGIN;
        const cy = Math.floor(i / COLS) * CH_HEIGHT * 2 + TOP_MARGIN;
        const char = image.bit6 ? bit6(i) : i;

        for (let y = 0; y < CH_HEIGHT * 2; y++) {
            const byte = glyphs[char * CH_HEIGHT + Math.floor(y / 2)];

            for (let x = 0; x < CH_WIDTH; x++) {
                const pixel = (byte & (1 << (x))) !== 0;
                const color = pixel ? (y % 2 === 0 ? 0xFF : 0xFF) : 0;
                const offset = ((cy + y) * WIDTH + cx + x) * 4;

                dst.data[offset + 0] = color;
                dst.data[offset + 1] = color;
                dst.data[offset + 2] = color;
                dst.data[offset + 3] = 0xFF;
            }
        }
    }

    const buffer = PNG.sync.write(dst, {});
    fs.writeFileSync(image.filename, buffer);
    console.log("Generated " + image.filename);
}

