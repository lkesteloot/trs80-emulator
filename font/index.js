const GLYPH = [];
for (const g of [
        // GLYPH_TRSEMU,
        // GLYPH_XTRS_CG1,
        GLYPH_XTRS_CG2,
        // GLYPH_XTRS_CG4,
        // GLYPH_XTRS_CG5,
        // GLYPH_XTRS_CG6
]) {

    GLYPH.push(...g);
}

const CH_WIDTH = 8; // 7 for Model I.
const CH_HEIGHT = 12;
const GLYPH_COUNT = GLYPH.length/CH_HEIGHT;
const COLS = 32;
const ROWS = Math.ceil(GLYPH_COUNT/COLS);
const WIDTH = CH_WIDTH*COLS;
const HEIGHT = CH_HEIGHT*ROWS*2;

const dst = new PNG({
    width: WIDTH,
    height: HEIGHT,
});

function bit6(i) {
    const bit5 = (i >> 5) & 1;
    const bit7 = (i >> 7) & 1;
    const bit6 = (bit5 | bit7) ^ 1;

    return (i & 0xBF) | (bit6 << 6);
}

for (let i = 0; i < GLYPH_COUNT; i++) {
    const cx = i%COLS*CH_WIDTH;
    const cy = Math.floor(i/COLS)*CH_HEIGHT*2;
    const char = true ? i : bit6(i);

    for (let y = 0; y < CH_HEIGHT*2; y++) {
        const byte = GLYPH[char*CH_HEIGHT + Math.floor(y/2)];

        for (let x = 0; x < CH_WIDTH; x++) {
            const pixel = (byte & (1 << (x))) !== 0;
            const color = pixel ? (y % 2 === 0 ? 0xFF : 0xFF) : 0;
            const offset = ((cy + y)*WIDTH + cx + x)*4;

            dst.data[offset + 0] = color;
            dst.data[offset + 1] = color;
            dst.data[offset + 2] = color;
            dst.data[offset + 3] = 0xFF;
        }
    }
}

const options = {};
const buffer = PNG.sync.write(dst, options);
fs.writeFileSync("out.png", buffer);

